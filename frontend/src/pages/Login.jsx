import { useState } from "react";
import { Link, useNavigate } from "react-router-dom";
import { ArrowLeft, Eye, EyeOff } from "lucide-react";
import Logo from "../assets/Logo";
import { useAuth } from "@/context/AuthContext";
import { toast } from "sonner";
import authApi from "@/api/authApi";

const Login = () => {
  const navigate = useNavigate();
  const { login } = useAuth();
  const [formData, setFormData] = useState({ email: "", password: "" });
  const [showPassword, setShowPassword] = useState(false);
  const [showResend, setShowResend] = useState(false);
  const [sending, setSending] = useState(false);

  const handleSubmit = async (e) => {
    e.preventDefault();

    try {
      const res = await login(formData);
      const user = res.data?.user;

      toast.success("Login Successful ðŸŽ‰");

      if (!user.hasCompletedOnboarding) {
        return navigate("/onboarding");
      }

      return navigate("/dashboard");
    } catch (err) {
      const error = err.response?.data;

      if (error?.error?.code === "EMAIL_NOT_VERIFIED") {
        toast.error(error.message || "Email not verified âŒ");
        setShowResend(true);
        return;
      }

      toast.error(error?.message || "Invalid email or password âŒ");
    }
  };

  const handleChange = (e) => {
    setFormData({
      ...formData,
      [e.target.name]: e.target.value,
    });
  };

  const handleResendLink = async () => {
    if (!formData.email) return toast.error("Enter your email first.");

    setSending(true);

    try {
      await authApi.resendVerification(formData.email);
      toast.success("Verification email sent again ðŸ“§");
    } catch (err) {
      toast.error(err.response?.data?.message || "Failed to resend");
    } finally {
      setSending(false);
    }
  };

  return (
    <div className="h-screen overflow-hidden bg-gradient-to-br from-accent/5 via-background to-primary/5 flex items-center justify-center px-4 relative">
      {/* Back to Home - Top Left */}
      <div className="absolute top-6 left-6 z-10 animate-slide-in-left">
        <Link
          to="/"
          className="inline-flex items-center gap-2 text-muted-foreground hover:text-accent transition-colors group"
        >
          <ArrowLeft className="w-5 h-5 group-hover:-translate-x-1 transition-transform" />
          <span className="font-medium">Back to Home</span>
        </Link>
      </div>

      <div className="w-full max-w-md animate-fade-in">
        {/* Logo and Header */}
        <div className="text-center mb-8">
          <div className="flex justify-center mb-4 animate-scale-in">
            <div className="bg-card p-4 rounded-2xl shadow-lg border border-border">
              <Logo height={48} width={48} className="fill-primary" />
            </div>
          </div>
          <h1 className="text-3xl font-bold text-foreground mb-2 animate-fade-in delay-100">
            Welcome Back
          </h1>
          <p className="text-muted-foreground animate-fade-in delay-200">
            Login to continue your fitness journey
          </p>
        </div>

        {/* Login Form */}
        <div className="bg-card/80 backdrop-blur-sm rounded-2xl shadow-xl border border-border p-8 animate-fade-in delay-300">
          <form onSubmit={handleSubmit} className="space-y-6">
            {/* Email Field */}
            <div>
              <label
                htmlFor="email"
                className="block text-sm font-medium text-gray-700 mb-2"
              >
                Email Address
              </label>
              <input
                type="email"
                id="email"
                name="email"
                value={formData.email}
                onChange={handleChange}
                required
                className="w-full px-4 py-3 bg-gray-50 border border-gray-200 rounded-lg focus:outline-none focus:ring-2 focus:ring-[#06D6A0] focus:border-transparent transition text-gray-900 placeholder:text-gray-400"
                placeholder="your@email.com"
              />
            </div>

            {/* Password Field */}
            <div>
              <label
                htmlFor="password"
                className="block text-sm font-medium text-gray-700 mb-2"
              >
                Password
              </label>
              <div className="relative">
                <input
                  type={showPassword ? "text" : "password"}
                  id="password"
                  name="password"
                  value={formData.password}
                  onChange={handleChange}
                  required
                  className="w-full px-4 py-3 bg-gray-50 border border-gray-200 rounded-lg focus:outline-none focus:ring-2 focus:ring-[#06D6A0] focus:border-transparent transition text-gray-900 placeholder:text-gray-400"
                  placeholder="â€¢â€¢â€¢â€¢â€¢â€¢â€¢â€¢"
                />
                <button
                  type="button"
                  onClick={() => setShowPassword(!showPassword)}
                  className="absolute right-3 top-1/2 -translate-y-1/2 text-gray-400 hover:text-[#073B4C] transition"
                >
                  {showPassword ? (
                    <EyeOff className="w-5 h-5" />
                  ) : (
                    <Eye className="w-5 h-5" />
                  )}
                </button>
              </div>
            </div>

            {/* Remember Me & Forgot Password */}
            <div className="flex items-center justify-between">
              <label className="flex items-center cursor-pointer">
                <input
                  type="checkbox"
                  className="w-4 h-4 text-[#06D6A0] bg-gray-50 border-gray-300 rounded focus:ring-2 focus:ring-[#06D6A0]"
                />
                <span className="ml-2 text-sm text-gray-600">Remember me</span>
              </label>
              <Link
                to="/forgot-password"
                className="text-sm text-[#073B4C] hover:text-[#06D6A0] transition font-medium"
              >
                Forgot password?
              </Link>
            </div>

            {/* Submit Button */}
            <button
              type="submit"
              className="w-full py-3.5 bg-linear-to-r from-[#06D6A0] to-[#073B4C] text-white rounded-lg font-semibold shadow-lg hover:shadow-xl transition-all hover:scale-[1.02] hover:-translate-y-0.5 active:scale-[0.98]"
            >
              Sign In
            </button>

            {/* Divider */}
            {/* <div className="relative py-3">
              <div className="absolute inset-0 flex items-center">
                <div className="w-full border-t border-gray-200"></div>
              </div>
              <div className="relative flex justify-center text-sm">
                <span className="px-3 bg-white text-gray-500">
                  Or continue with
                </span>
              </div>
            </div> */}

            {/* Google Login Only */}
            {/* <button
              type="button"
              className="w-full flex items-center justify-center gap-3 px-4 py-3 bg-white border-2 border-gray-200 rounded-lg hover:border-gray-300 hover:shadow-md transition-all text-gray-700 font-medium hover:scale-[1.02] active:scale-[0.98]"
            >
              <svg className="w-5 h-5" viewBox="0 0 24 24">
                <path
                  fill="#4285F4"
                  d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
                />
                <path
                  fill="#34A853"
                  d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
                />
                <path
                  fill="#FBBC05"
                  d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
                />
                <path
                  fill="#EA4335"
                  d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
                />
              </svg>
              Continue with Google
            </button> */}
          </form>

          {/* ---- RESEND SECTION ---- */}
          {showResend && (
            <div className="mt-4 text-center text-sm text-gray-600">
              Didnâ€™t receive the verification email?
              <button
                disabled={sending}
                onClick={handleResendLink}
                className="text-[#06D6A0] font-semibold ml-1 hover:underline"
              >
                {sending ? "Sending..." : "Resend email"}
              </button>
            </div>
          )}

          {/* Sign Up Link */}
          <div className="mt-6 text-center">
            <p className="text-gray-600">
              Don't have an account?{" "}
              <Link
                to="/signup"
                className="text-[#073B4C] font-semibold hover:text-[#06D6A0] transition"
              >
                Sign up
              </Link>
            </p>
          </div>
        </div>
      </div>
    </div>
  );
};

export default Login;
